    {
        #validation
        $request->validate([
            'comment_body'.$id => 'required|min:1|max:1000',
        ]);

        // dump();

        $this->comment->user_id = Auth::user()->id;
        $this->comment->post_id = $id;
        $this->comment->body = $request->input('comment_body'.$id);

        $this->comment->save();

        return redirect()->back();
    }

